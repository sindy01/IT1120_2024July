class AArray1D {
	
    public static void main(String[] args) {
		
        int numbers[] = {10, 258, -57, 1587};
        
		//Print the First Element in the Array
		System.out.println("First Element: " + numbers[0]); //10
		
		//Print the Second Element in the Array
		System.out.println("Second Element: " + numbers[1]); //258
		
		//Add 2 to the Second Element of the Array
		numbers[1] += 2;
		System.out.println("Updated Second Element: " + numbers[1]); //
		 
    }
}