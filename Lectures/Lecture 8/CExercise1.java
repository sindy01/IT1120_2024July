class CExercise1 {
    public static void main(String[] args) {
		
		//Exercise 1A
		double numberA = 7.5;
		
		System.out.println(numberA + " floor is " + Math.floor(numberA));

		System.out.println();
		
		//Exercise 1B
		double numberB = -3.6;
		
		System.out.println(numberB + " abs is " + Math.abs(numberB));
		
		System.out.println();
		
		//Exercise 1C
		double numberC = 0.0;
		
		System.out.println(numberC + " floor is " + Math.floor(numberC));
		
		System.out.println();

		//Exercise 1D
		double numberD = -6.4;
		
		System.out.println(numberD + " ceil is " + Math.ceil(numberD));
		
		System.out.println();
		
		//Exercise 1E
		double base = 5.0;
		double exponent = 2.0;
		
		System.out.println("Power is " + Math.pow(base, exponent));
		
		System.out.println();
		
		//Exercise 1F
		double numberF = 2.7;
		
		System.out.println(numberF + " round is " + Math.round(numberF));
		
		System.out.println();
		
		//Exercise 1G
		double numberG = 2.5;
		System.out.println(numberG + " abs is " + Math.abs(numberG));
		
    }
}